package main;

public class Glossary_Item {
	private int id;
	private String word;
	private String defn;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getWord() {
		return word;
	}
	public void setWord(String word) {
		this.word = word;
	}
	public String getDefn() {
		return defn;
	}
	public void setDefn(String defn) {
		this.defn = defn;
	}
	
}
